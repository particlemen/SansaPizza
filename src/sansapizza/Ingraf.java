    private void Comprar_mesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Comprar_mesaActionPerformed
        if (local.getMesas().size() == 9) {
            JOptionPane.showMessageDialog(this, "No hay espacio para mesas, vende alguna antes de comprar");
            return;
        }
        String[] options = new String[] {"Comprar Mesa Chica", "Comprar Mesa Mediana", "Comprar Mesa Grande"};
        int respuesta = JOptionPane.showOptionDialog(null, "¿Que Tipo de Mesa Deseas Comprar?", "Comprar Mesas",
        JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
        null, options, options[0]);
        local.anadirMesa(respuesta);
    }//GEN-LAST:event_Comprar_mesaActionPerformed

