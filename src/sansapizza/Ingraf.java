    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        local.iniciarDia();
        this.ActualizarEstado();
        if (local.getDineroLocal() <= 0) JOptionPane.showMessageDialog(this, "Perdiste el juego, no te queda mas plata, tu vida quedo destrozada,\nlo unico que te quedan son los sueños en donde todavia puedes salvar a tu pizzeria...\n¿Quien sabe si logras salvarla alli?");
        if (local.getEstrellas() == 5) JOptionPane.showMessageDialog(this, " Ganaste el juego, Felicidades, puedes seguir jugando para hacer que tu pizzeria sea una de la cual don Fede este orgulloso");
    }//GEN-LAST:event_jToggleButton1ActionPerformed
    
    private void ActualizarEstado(){
       this.informacion1 = "Estrellas Actuales:" + Integer.toString(local.getEstrellas());
       this.informacion2 = "Popularidad Actual:" + Integer.toString((int)local.getPopularidad());
       this.informacion3 = "Dinero Actual:" + Integer.toString(local.getDineroLocal());
       this.informacion4 = "Clientes Felices:" + Integer.toString(local.getLastDay().getCf());
       this.Estrellas.setText(this.informacion1);
       this.Popularidad.setText(this.informacion2);
       this.Dinero.setText(this.informacion3);
       this.ClientesFelices.setText(this.informacion4);
    }
